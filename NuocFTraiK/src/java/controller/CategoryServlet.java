    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String cid_raw = request.getParameter("cid");
        int categoryId;
        if (cid_raw == null) {
            categoryId = 1;
        } else {
            categoryId = Integer.parseInt(cid_raw);
        }
        DAO dao = new DAO();
        List<Product> listP = dao.getProductsByCategoryId(categoryId);

        //List Category
        List<Category> listC = dao.getAllCategory();
        request.setAttribute("listC", listC);    //list category

        //Phan trang
        int page, numperpage = 8;
        int num = (int) Math.ceil(listP.size() / 8.0);
        String xpage = request.getParameter("page");
        if (xpage == null) {
            page = 1;
        } else {
            page = Integer.parseInt(xpage);
        }
        int start, end;
        start = (page - 1) * numperpage;
        end = Math.min(page * numperpage, listP.size());

        List<Product> list = dao.getListByPage(listP, start, end);
        //B2:Day data len jsp
        request.setAttribute("listP", list);
        request.setAttribute("tag", categoryId); //de active category
        request.setAttribute("page", page);
        request.setAttribute("num", num);
        request.getRequestDispatcher("homeTab.jsp").forward(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        processRequest(request, response);
    }

